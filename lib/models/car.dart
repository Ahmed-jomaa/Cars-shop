class Car {
  final String id;
  final String country;
  final String title;
  final List<String> description;
  final String status;
  final String year;
  final String price;
  final bool isSUV;
  final bool isElectric;

  Car({
      required this.id,
      required this.country,
      required this.title,
      required this.description,
      required this.status,
      required this.year,
      required this.price,
      required this.isSUV,
      required this.isElectric
      });
}
